extension UIFont {
    
    public static func roboto(_ fontSize: CGFloat, weight: UIFont.Weight) -> UIFont {
        FontManager.registerRobotoFontsIfNeeded()
        let fontName: String
        switch weight {
        case .bold:
            fontName = "Roboto-Bold"
        case .medium:
            fontName = "Roboto-Medium"
        default:
            fontName = "Roboto-Regular"
        }
        return UIFont(name: fontName, size: fontSize) ?? .systemFont(ofSize: fontSize, weight: weight)
    }
    
    public static func italicRoboto(_ fontSize: CGFloat) -> UIFont {
        FontManager.registerRobotoFontsIfNeeded()
        let fontName: String = "Roboto-Italic"
        return UIFont(name: fontName, size: fontSize) ?? .italicSystemFont(ofSize: fontSize)
    }
}

private enum FontManager {
    
    private static var isFontRegistered: Bool = false
    
    static func registerRobotoFontsIfNeeded() {
        if isFontRegistered {
            return
        }
        let fonts = [
            "Roboto-Bold",
            "Roboto-Medium",
            "Roboto-Regular",
            "Roboto-Italic",
        ]
        
        for font in fonts {
            guard let fontURL = Bundle.module.url(forResource: font, withExtension: "ttf") else {
                print("\(font).ttf not found.")
                continue
            }
            var error: Unmanaged<CFError>?
            CTFontManagerRegisterFontsForURL(fontURL as CFURL, .process, &error)
            
            if let error {
                print("Font registration failed: \(error.takeUnretainedValue().localizedDescription)")
            }
        }
        isFontRegistered = true
    }
}
